QUnit.test( "Cuboid Test", function( assert ) {
  assert.equal( Shapes.cuboid(.5,.5,.5),
  {
  	vertices: [
  	[ .25, .25, .25 ],
    [ -.25, .25, .25 ],
    [ .25, -.25, .25 ],
    [ .25, .25, -.25 ],
    [ -.25, -.25, .25 ],
    [ -.25, .25, -.25 ],
    [ .25, -.25, -.25 ],
    [-.25, -.25, -.25 ],
  	],
  	indices: [
  	[ 0, 3, 6 ],
    [ 0, 2, 6 ],
    [ 1, 5, 7 ],
    [ 1, 4, 7 ],
    [ 0, 3, 5 ],
    [ 0, 1, 5 ],
    [ 2, 6, 7 ],
    [ 2, 4, 7 ],
    [ 3, 6, 7 ],
    [ 3, 5, 7 ],
    [ 0, 2, 4 ],
    [ 0, 1, 4 ],
  	]
  } );
});